import Foundation
import Combine

extension Publisher where Failure == Never {
    func assign<Root: AnyObject>(to keyPath: ReferenceWritableKeyPath<Root, Output>, on root: Root) -> AnyCancellable {
        return sink { [weak root] in
            root?[keyPath: keyPath] = $0
        }
    }
}
